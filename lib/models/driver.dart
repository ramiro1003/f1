class Driver {
  final int number;
  final String country;
  final String name;
  final int position;
  final String team;
  final int standing;
  final double points;
  final int podiums;
  final int fastestLaps;
  final DateTime birth;

  Driver({required this.number,
    required this.name,
    required this.position,
    required this.team,
  required this.standing,
  required this.country,
    required this.points,
  required this.podiums,
  required this.fastestLaps,
  required this.birth});

}